# # Creating a New Project
#
# Creates project to be used as a home for the nebula resources of tasks and workflows.
# Refer to the [nebulactl API reference](https://docs.nebula.org/projects/nebulactl/en/stable/gen/nebulactl_create_project.html)
# for more details.
#
# ```{eval-rst}
# .. prompt:: bash
#
#   nebulactl create project --id "my-nebula-project-name" --labels "my-label=my-project-label"  --description "my-nebula-project-name" --name "my-nebula-project-name"
# ```
